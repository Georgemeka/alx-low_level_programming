
/**
 * op_add - returns sum of a an b
 * @a: 1st parameter of function
 * @b: 2nd parameter of function
 * Return: sum of numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of a and b
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds modulus of two numbers
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
